#ifndef Exception_H
#define Exception_H

#include <iostream>
#include <string>

/** An alternative to the C++ exception class. Contains a human-readable error message.
 */
class Exception {
public:
	/** Construct a new Exception object with the specified message.
	 */
	Exception(const std::string message);

	/** Construct a new Exception object with the specified message and function.
	 */
	Exception(const std::string message, const std::string function);

	/** Deletes the message attribtue.
	 */
	~Exception();

	/** Prints the Exception message to cerr for debugging purposes.
	 */
	virtual void print();

	/**
	 */
	std::string* getMessage();
private:
	// A string object representing the message generated by the exception.
	std::string* message;

	// A string object representing which function threw the exception.
	std::string* function;
};

#endif // Exception_H
